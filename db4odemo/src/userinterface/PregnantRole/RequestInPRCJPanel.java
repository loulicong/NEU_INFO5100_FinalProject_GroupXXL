    private void SubmitActionPerformed(ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this,"please select a PRC");
            return;
        }
        for(InRequest s:system.getInRequestList().getRequestList()){
            if(s.getSender().getUsername().equals(userAccount.getUsername())&&
                    s.getPrc().getName().equals(system.getPRCDirectory().getPRCList().get(selectedRow).getName())){
                if(s.getStatus().equals("Processing")||s.getStatus().equals("In PRC")||s.getStatus().equals("Requesting")){
                    JOptionPane.showMessageDialog(this,"You have requested this PRC!");
                    return;
                }
            }
        }
        SimpleDateFormat sdf = new SimpleDateFormat();// 格式化时间
        sdf.applyPattern("yyyy-MM-dd HH:mm:ss a");// a为am/pm的标记
        Date date = new Date();// 获取当前时间
        //System.out.println("现在时间：" + sdf.format(date));

        system.getInRequestList().createRequest(userAccount, null, "Processing", sdf.format(date), null, null,
                system.getPRCDirectory().getPRCList().get(selectedRow));
//        InRequest requestin=userAccount.getPRCinRequestList().createRequest(userAccount, null, "Processing", sdf.format(date), null, null,
//                system.getPRCDirectory().getPRCtArrayList().get(selectedRow));
//
        system.getPRCDirectory().getPRCList().get(selectedRow);
        JOptionPane.showMessageDialog(this,"Processing!");


    }//GEN-LAST:event_SubmitActionPerformed

