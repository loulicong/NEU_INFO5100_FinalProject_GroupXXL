    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(PRC prc:system.getPRCDirectory().getPRCList()){
            Object[] row = new Object[2];
            row[0]=prc;
            row[1]=prc.getCommunity().name;
            model.addRow(row);
        }

    }
