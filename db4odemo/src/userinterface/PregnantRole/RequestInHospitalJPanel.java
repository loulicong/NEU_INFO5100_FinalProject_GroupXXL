    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Hospital h:system.getHospitalDirectory().getHospitalArrayList()){
            Object[] row = new Object[3];
            row[0]=h;
            row[1]=h.getCommunity().name;

            model.addRow(row);
        }

    }
