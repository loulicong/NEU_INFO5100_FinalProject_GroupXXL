    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this,"please select a hospital");
            return;
        }
        if(txt_PrePeriod.getText().equals("")){
            JOptionPane.showMessageDialog(this,"please input your pregnant period");
            return;
        }
        for(InRequest s:system.getInRequestList().getRequestList()){
            if(s.getSender().getUsername().equals(userAccount.getUsername())&&
                    s.getHospital().getName().equals(system.getHospitalDirectory().getHospitalArrayList().get(selectedRow).getName())){
                if(s.getStatus().equals("Processing")||s.getStatus().equals("In hospital")||s.getStatus().equals("Requesting")){
                    JOptionPane.showMessageDialog(this,"You have requested this hospital!");
                    return;
                }
            }
        }
        SimpleDateFormat sdf = new SimpleDateFormat();// 格式化时间
        sdf.applyPattern("yyyy-MM-dd HH:mm:ss a");// a为am/pm的标记
        Date date = new Date();// 获取当前时间
        //System.out.println("现在时间：" + sdf.format(date));
        InRequest inRequest = system.getInRequestList().createRequest(userAccount, txt_PrePeriod.getText(), "Requesting",
                sdf.format(date),null,system.getHospitalDirectory().getHospitalArrayList().get(selectedRow),null);
//        InRequest requestin=userAccount.getHospitalinRequestListt().createRequest(userAccount,
//                txt_PrePeriod.getText(),"Requesting",
//                sdf.format(date),null,system.getHospitalDirectory().getHospitalArrayList().get(selectedRow),null);
        system.getHospitalDirectory().getHospitalArrayList().get(selectedRow).getInrequestDirectory().getRequestList().add(inRequest);
//
        txt_PrePeriod.setText("");
        JOptionPane.showMessageDialog(this,"Processing!");


    }//GEN-LAST:event_SubmitActionPerformed

