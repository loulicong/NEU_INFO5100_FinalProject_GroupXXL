    private void deleteJButtonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_refreshTestJButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel)communityJTable.getModel();
        int selectedRow = communityJTable.getSelectedRow();
        int id = (Integer) model.getValueAt(selectedRow,1);
        InRequest s = business.getInRequestList().searchById(id);
        business.getInRequestList().getRequestList().remove(s);
    }

