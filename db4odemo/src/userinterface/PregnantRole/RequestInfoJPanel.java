    public void populateRequestTable(){
        DefaultTableModel model_1 = (DefaultTableModel) communityJTable.getModel();
        model_1.setRowCount(0);
        for(InRequest r:business.getInRequestList().getRequestList()){
            if(r.getSender().getUsername().equals(userAccount.getUsername())){
                if(r.getHospital()!=null){
                    Object[] row = new Object[4];
                    row[2]=r.getHospital().getName();
                    row[1]=r.getRequestDate();
                    row[3]=r.getStatus();
                    row[0]=r.getId();
                    model_1.addRow(row);
                }
                if(r.getPrc()!=null){
                    Object[] row = new Object[4];
                    row[2]=r.getPrc().getName();
                    row[1]=r.getRequestDate();
                    row[3]=r.getStatus();
                    row[0]=r.getId();
                    model_1.addRow(row);
                }

            }
        }
    }

    
