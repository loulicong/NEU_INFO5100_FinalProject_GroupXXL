    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow=jTable1.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this,"please select a info");
            return;
        }
        PregnantInfo pregnantinfo=pregnant.getPregnantInfoArrayList().get(selectedRow);
        int width=240, height=150;
        ImageIcon img=new ImageIcon(pregnantinfo.getImage());
        img.setImage(img.getImage().getScaledInstance(width, height, Image.SCALE_DEFAULT));
        jLabel3.setIcon(img);
        jLabel4.setText(pregnantinfo.getDescription());

    }//GEN-LAST:event_jButton1ActionPerformed

