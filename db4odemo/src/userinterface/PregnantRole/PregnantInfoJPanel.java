    private void populateRequestTable() {
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(PregnantInfo pregnantinfo:pregnant.getPregnantInfoArrayList()){
                Object[] row = new Object[2];
                row[0]=pregnantinfo.getCheckdate();
                row[1]=pregnantinfo.getDescription();
                model.addRow(row);
                            
        }
          
    }

    
